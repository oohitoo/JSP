package movieborad;

public class Champion {

	private String idx;
	private String userID;
	private String championDate;
	private String championContent;
	
	public String getIdx() {
		return idx;
	}
	public void setIdx(String idx) {
		this.idx = idx;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getChampionDate() {
		return championDate;
	}
	public void setChampionDate(String championDate) {
		this.championDate = championDate;
	}
	public String getChampionContent() {
		return championContent;
	}
	public void setChampionContent(String championContent) {
		this.championContent = championContent;
	}
	
	
	
}
