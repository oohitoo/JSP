package movieborad;

public class wrestler {

	private String idx;
	private String userID;
	private String wrestlerDate;
	private String wrestlerContent;
	
	public String getIdx() {
		return idx;
	}
	public void setIdx(String idx) {
		this.idx = idx;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getWrestlerDate() {
		return wrestlerDate;
	}
	public void setWrestlerDate(String wrestlerDate) {
		this.wrestlerDate = wrestlerDate;
	}
	public String getWrestlerContent() {
		return wrestlerContent;
	}
	public void setWrestlerContent(String wrestlerContent) {
		this.wrestlerContent = wrestlerContent;
	}
	
	
	
}
